/* PWM Interrupt handlers */
#ifdef TARGET_KIT_XMC14_BOOT_001
void IRQ26_Handler(void)
{
  g_num_period_interrupts++;
  XMC_CCU8_SLICE_ClearEvent(SYMMETRIC_PWM_SLICE_PTR, XMC_CCU8_SLICE_IRQ_ID_PERIOD_MATCH);
}
#endif
